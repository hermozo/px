        $arr = $request['info'];
        $porciones = explode("&", $arr);
        foreach ($porciones as $key => $val) {
            $sql = 'update multimedia set orden = ' . $key . ' where id = ' . str_replace("item[]=", "", $val);
            DB::select($sql);
        }
        return ['resp' => true];
    }

